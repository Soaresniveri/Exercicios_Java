public class ExibeValores {
	
	public static void main (String[] args) {
		byte Byte_var = 19;
		short Short_var = 1571;
		int Int_var = 100000;
		long Long_var = 2147483648l;
		float Float_var = 3.14f;
		double Double_var = 3.141592653589793;
		boolean True_var = true;
		char Char_var = 'K';
		
		System.out.print(Byte_var + "\n"
						+ Short_var + "\n"
						+ Int_var + "\n"
						+ Long_var + "\n"
						+ Float_var + "\n"
						+ Double_var + "\n"
						+ True_var + "\n"
						+ Char_var );
	}
}

